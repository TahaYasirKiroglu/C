int encrypt(int chr1, int chr2){
	int sum;
	if(isalpha (chr1) && isalpha (chr2)){ /*if chr1 and chr2 alpha*/
		chr1 = toupper (chr1);
		chr2 = toupper (chr2);
		sum = ( (chr1 + chr2 - 130) % 26) + 65;
	} else 
		sum = ' '; /* if chr 1 and chr2 not alpha make it space*/
	return sum;
}
