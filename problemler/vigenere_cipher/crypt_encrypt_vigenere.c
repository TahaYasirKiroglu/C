/* verilen formüller, soruda verilen tablonun formülize edilmesiyle ortaya çıkmıştır */
int decrypt(int crypt, int cipher){
	int sum;
	if(isalpha (crypt) && isalpha (cipher)){
		crypt = toupper (crypt);
		cipher = toupper (cipher);
		sum = ( (26 + crypt - cipher ) % 26) + 65;
	} else 
		sum = ' '; /* if chr 1 and chr2 not alpha make it space*/
	return sum;
}
